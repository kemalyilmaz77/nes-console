import 'package:flutter/material.dart';

class ButtonMode {

  final Widget? widget;
  final double? x;
  final double? y;
  final double? height;
  final double? width;
  final double? borderRadius;
  final Color? color;
  final String? key;

  ButtonMode({this.widget,this.key,this.x,this.y,this.color, this.height, this.width,this.borderRadius});
}